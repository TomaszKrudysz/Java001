public class Op3_relational_operators {
    public static void main(String[] args) {
        boolean b1 = 5 == 3;
        System.out.println(b1);
        boolean b2 = 7 == 7;
        System.out.println(b2);

        boolean b3 = 7 != 7;
        System.out.println(b3);
        boolean b4 = 5 != 5;
        System.out.println(b4);

        boolean b5 = 10 > 4;
        System.out.println(b5);

        boolean b6 = 8 >= 8;
        System.out.println(b6);


    }
}
